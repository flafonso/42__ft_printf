#include "libft.h"

int		ft_sqrt_supp(int nb)
{
	int		i;

	i = 1;
	if (nb <= 0)
		return (0);
	while (i < 463441)
	{
		if (i * i >= nb)
			return (i);
		i++;
	}
	return (0);
}
