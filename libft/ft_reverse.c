#include "libft.h"

void	ft_reverse(char **str)
{
	char	c;
	int		l;
	int		i;

	l = 0;
	i = 0;
	while (str[0][l] != '\0')
		l++;
	l--;
	while (i < l)
	{
		c = str[0][i];
		str[0][i] = str[0][l];
		str[0][l] = c;
		i++;
		l--;
	}
}
